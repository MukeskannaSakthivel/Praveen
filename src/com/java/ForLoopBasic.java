package com.java;

public class ForLoopBasic {
	public static void main(String[] args) {
		System.out.println("********************************");
		int count = 1;
		for(int i=4;i<=40;i=i+4) {
			System.out.println(count+" X 4 = "+i );
			count++;
		}
		System.out.println("********************************");
	}
}
